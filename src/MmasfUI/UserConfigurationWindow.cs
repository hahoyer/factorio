using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Linq;
using System.Threading;
using System.Threading.Tasks;
using System.Windows;
using System.Windows.Controls;
using System.Windows.Data;
using hw.DebugFormatter;
using hw.Helper;
using ManageModsAndSavefiles;
using ManageModsAndSavefiles.Saves;
using MmasfUI.Common;

namespace MmasfUI
{
    sealed class UserConfigurationWindow : Window
    {
        readonly UserConfiguration Configuration;
        bool IsSaves;
        readonly FileClusterProxy[] Data;

        public UserConfigurationWindow(FileConfiguration fileConfiguration)
        {
            var configuration = MmasfContext
                .Instance
                .UserConfigurations.Single(u => u.Name == fileConfiguration.FileName);

            IsSaves = fileConfiguration.Type == FileConfiguration.SavesType;
            Configuration = configuration;

            Data = Configuration
                .SaveFiles
                .Select(s => new FileClusterProxy(s))
                .ToArray();

            Content = CreateGrid();

            Title = fileConfiguration.Type + " of " + configuration.Name.Quote();
            this.InstallPositionPersister(fileConfiguration.PositionPath);
            this.InstallMainMenu(CreateConfigurationMenu());
        }

        ScrollViewer CreateGrid()
        {
            var result = new DataGrid
            {
                ItemsSource = Data,
                IsReadOnly = true
            };
            result.AutoGeneratingColumn += (s, e) => OnAutoGeneratingColumns((DataGrid) s, e);
            result.AutoGeneratedColumns += (s, e) => OnAutoGeneratedColumns((DataGrid) s);

            Task.Factory.StartNew
            (
                () =>
                {
                    Thread.CurrentThread.Priority = ThreadPriority.AboveNormal;
                    "waiting".WriteFlaggedLine();
                    100.MilliSeconds().Sleep();

                    Tracer.FlaggedLine("Refreshing");
                    RefreshData();
                    Tracer.FlaggedLine("Refreshed");
                }
            );

            return new ScrollViewer
            {
                Content = result,
                VerticalScrollBarVisibility = ScrollBarVisibility.Auto,
                HorizontalScrollBarVisibility = ScrollBarVisibility.Auto
            };
        }

        static void OnAutoGeneratingColumns
        (
            DataGrid grid,
            DataGridAutoGeneratingColumnEventArgs args)
        {
            if(args.PropertyType != typeof(MyTimeSpan))
                return;

            var column = (DataGridTextColumn) args.Column;
            column.SortMemberPath += ".Value";
            ((Binding) column.Binding).Path.Path += ".DisplayValue";
            column.CanUserSort = true;
        }


        static void OnAutoGeneratedColumns(DataGrid grid) { }

        void RefreshData()
        {
            Tracer.FlaggedLine("loop");
            Parallel.ForEach(Data, proxy => proxy.Refresh());
        }

        public sealed class MyTimeSpan : DumpableObject, INotifyPropertyChanged
        {
            public TimeSpan Value { get; set; }

            public MyTimeSpan(TimeSpan value)
            {
                Value = value;
                OnPropertyChanged();
            }

            public string DisplayValue => Value.Format3Digits();
            public override string ToString() => Value.Format3Digits();

            public event PropertyChangedEventHandler PropertyChanged;

            void OnPropertyChanged()
                => PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(null));
        }

        sealed class FileClusterProxy : INotifyPropertyChanged
        {
            readonly FileCluster FileCluster;

            public string Name => FileCluster.Name;

            public Version Version { get; set; }
            public MyTimeSpan Duration { get; set; }

            public FileClusterProxy(FileCluster fileCluster) { FileCluster = fileCluster; }

            public void Refresh()
            {
                Version = FileCluster.Version;
                Duration = new MyTimeSpan(FileCluster.Duration);
                OnPropertyChanged();
            }

            public event PropertyChangedEventHandler PropertyChanged;

            void OnPropertyChanged()
                => PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(null));
        }

        static Menu CreateConfigurationMenu()
            => new Menu
            {
                Items =
                {
                    new MenuItem
                    {
                        Header = "_File",
                        Items =
                        {
                            "_Exit".MenuItem("Exit")
                        }
                    }
                }
            };
    }
}